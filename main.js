var day = "saturday";
switch (day) {
    case "Monday":
        console.log("Its your work day, do your assignments.");
        break;
    case "sunday":
        console.log("Its your off, have fun.");
        break;
    case "saturday":
        console.log("Its your class at Governor house by 9am.");
        break;
    case "Friday":
        console.log("Today is friday, manage some time for prayers.");
        break;
    default:
        console.log("Invalid input");
}
